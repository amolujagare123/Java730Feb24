package Inheritance.Polymorphism;

public class Casting {

    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        double c = (double)   a/b; // type casting

        // when you want to store one type of value into another type
        // we write that type in front of the value to be
        // stored in round brackets
        // this is called as type casting

        System.out.println("c="+c);
    }
}
